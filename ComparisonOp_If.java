package JavaPrograms;

public class ComparisonOp_If {

	public static void main(String[] args) 
	{
		int a = 27, b = 92 ;
		if(a>b)//greater than
		{
			System.out.println("a is greater than b");
		}
		if(a<b)//less than
		{
			System.out.println("a is smaller than b");
		}if(a>=b)//greater than or equals to
		{
			System.out.println("a is greater than or equals to  b");
		}if(a<=b)//less than or equals to
		{
			System.out.println("a is lesser than or equals to b");
		}if(a==b)//equals to
		{
			System.out.println("a is equals to b");
		}if(a!=b)//not equals to
		{
			System.out.println("a is not equals to b");
		}
	}

}

